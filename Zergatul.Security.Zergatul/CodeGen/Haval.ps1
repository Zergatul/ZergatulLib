function hex($params)
{
    $i = $params[0]
    $digits = $params[1]
    if (-not $digits)
    {
        $digits = 1
    }
    return [System.Convert]::ToString($i, 16).PadLeft($digits, '0')
}

function F1($params)
{
    $x6 = $params[0]
    $x5 = $params[1]
    $x4 = $params[2]
    $x3 = $params[3]
    $x2 = $params[4]
    $x1 = $params[5]
    $x0 = $params[6]

    "($x1 & ($x0 ^ $x4)) ^ ($x2 & $x5) ^ ($x3 & $x6) ^ $x0"
}

