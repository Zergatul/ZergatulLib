            s34 = 0;
            s40 = 0;
            s41 = 0;
            s42 = 0;
            s43 = 0;
            s44 = 0;

            bufOffset = 0;
        }

        public override byte[] Digest()
        {
            Close(BlockLength);
            return InternalStateToDigest();
        }

        protected override void ProcessBlock()
        {
            Core(BlockLength);
        }

        protected abstract byte[] InternalStateToDigest();

